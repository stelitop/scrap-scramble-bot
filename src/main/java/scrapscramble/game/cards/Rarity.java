package scrapscramble.game.cards;

/**
 * Describes the rarity of a card in the game.
 */
public enum Rarity {
    /**
     * Indicates no rarity for the card.
     */
    None,
    /**
     * Common rarity.
     */
    Common,
    /**
     * Rare rarity.
     */
    Rare,
    /**
     * Epic rarity.
     */
    Epic,
    /**
     * Legendary rarity.
     */
    Legendary
}
